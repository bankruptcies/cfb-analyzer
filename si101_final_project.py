# Function to filter player stats data so it only includes the two teams in a given game
def filter_player_stats_by_teams(player_data, game):
  # Extract and normalize the home and away team names from the game
  home_team = game.get("homeTeam", "").lower()
  away_team = game.get("awayTeam", "").lower()

  # List to store filtered team data
  filtered = []

  # Set to track which teams have already been added (prevents duplicates)
  seen_teams = set()

  # Loop through each game block in the player data
  for game_block in player_data:
    # Each game block may contain multiple teams
    for team in game_block.get("teams", []):
      # Extract and normalize the team name
      team_name = team.get("team", "").strip().lower()

      # Check if the team matches either the home or away team
      # and has not already been added
      if (team_name == home_team or team_name == away_team) and team_name not in seen_teams:
        # Add the matching team to the filtered list
        filtered.append(team)

        # Mark this team as seen to avoid duplicates
        seen_teams.add(team_name)

  # Return the filtered list of team stats
  return filtered

# Function to determine statistical leaders (passing, rushing, receiving) from filtered data
def get_stat_leaders(filtered_data):
  # Initialize leaders dictionary with default values
  # Each category stores a tuple: (player_name, stat_value)
  leaders = {
      "passing": ("None", 0),
      "rushing": ("None", 0),
      "receiving": ("None", 0)
  }

  # Loop through each team's data in the filtered dataset
  for team in filtered_data:
    # Each team contains multiple statistical categories (e.g., passing, rushing)
    for category in team.get("categories", []):
      # Name of the category
      category_name = category.get("name")

      # Each category contains different stat types (e.g., YDS, TDs, etc.)
      for stat_type in category.get("types", []):
        stat_name = stat_type.get("name")

        # Only consider yardage stats ("YDS")
        if stat_name != "YDS":
          continue

        # Loop through each athlete in the stat type
        for athlete in stat_type.get("athletes", []):
          name = athlete.get("name")
          stat = athlete.get("stat")

          # Attempt to convert stat to an integer
          try:
            stat = int(stat)
          except:
            continue

          # Update passing leader if this stat is higher
          if category_name == "passing":
            if stat > leaders["passing"][1]:
              leaders["passing"] = (name, stat)

          # Update rushing leader if this stat is higher
          elif category_name == "rushing":
            if stat > leaders["rushing"][1]:
              leaders["rushing"] = (name, stat)

          # Update receiving leader if this stat is higher
          elif category_name == "receiving":
            if stat > leaders["receiving"][1]:
              leaders["receiving"] = (name, stat)

  # Return the dictionary containing stat leaders for each category
  return leaders


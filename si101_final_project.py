# Function to calculate and display a team's performance for a given week
def team_week_stats(team_games, team, week):
  # Initialize counters for wins, losses, and total points scored
  wins = 0
  losses = 0
  points_scored = 0

  # Print a formatted header for the weekly summary
  print(f"\n{team.title()} Weekly Summary - Week {week}")
  print("-" * 50)

  # Loop through each game involving the team
  for game in team_games:
    # Extract team names and scores from the game data
    home_team = game.get("homeTeam", "")
    away_team = game.get("awayTeam", "")
    home_points = game.get("homePoints")
    away_points = game.get("awayPoints")

    # Skip games where scores are not available (e.g., not yet played)
    if home_points is None or away_points is None:
      continue

    # Determine if the selected team is the home or away team
    if home_team.lower() == team.lower():
      opponent = away_team
      team_points = home_points
      opp_points = away_points
    else:
      opponent = home_team
      team_points = away_points
      opp_points = home_points

    # Add this game's points to the total points scored
    points_scored += team_points

    # Determine if the team won or lost and update counters
    if team_points > opp_points:
      print(f"W vs {opponent} ({team_points}-{opp_points})")
      wins += 1
    else:
      print(f"L vs {opponent} ({team_points}-{opp_points})")
      losses += 1

  # Print a summary of results
  print("-" * 50)
  print(f"Games Played: {len(team_games)}")
  print(f"Wins: {wins} | Losses: {losses}")
  print(f"Total Points Scored: {points_scored}")

